  Path picked: option (b) â€” mirror the route's markup directly.
  ------------------------------------------------------------
  Per the dispatch's harness selection note (option a vs option b),
  the SvelteKit route page lives at
  `frontend/src/routes/(public)/s/[token]/+page.svelte`. Importing
  it from a CT harness is technically possible (the CT bundler
  preprocesses TS, so the `import type { PageData } from './$types'`
  line is erased at build time), but `./$types` is generated by
  `svelte-kit sync` and dragging Kit's virtual route graph into the
  CT bundler is the kind of cross-tooling coupling that breaks on
  the first refactor. Mirroring the route's markup verbatim keeps
  the harness self-contained: the spec exercises the same
  `MessageList` + `formatRelativeTime` + class structure the route
  renders, but without depending on Kit being able to satisfy the
  route file's type imports.

  If a future refactor extracts the route's markup into a real
  presentational component (`SharedSnapshotView.svelte` under
  `$lib/components/share/`), this harness should switch to mounting
  that component directly â€” option (a) becomes correct the moment
  the surface stops being route-only.

  Behaviour the harness exposes
  -----------------------------
  - `snapshot: SharedChatSnapshot | null` prop: `null` renders the
    dead-link panel; otherwise renders the title + subline + the
    M2 `MessageList` in `readonly` mode.
  - `now: number` prop: optional fixed clock injected into
    `formatRelativeTime` so the relative-time subline doesn't
    drift between test runs. Defaults to `snapshot.created_at + 60s`
    so the subline is deterministically `"a minute ago"`-shaped.
  - No `ActiveChatStore` context is provisioned: `MessageList` in
    `readonly` mode walks the readonly path that does NOT call
    `getContext` for it (M3 contract â€” the public share view never
    pulls in the (app) layout's stores).
  - A `ThemeStore` IS provisioned because the recursive Markdown
    subtree (`CodeBlock` -> Shiki, `ColonFenceBlock` -> Mermaid)
    reads it for the preset-aware syntax-highlighting cascade.
    Mirrors `MarkdownHarness.svelte`'s setup.

  Fixture data lives in `share-fixtures.ts` (sibling) so the spec
  can re-use the same constants and factories. Existing CT
  harnesses in this repo expose only their default component
  export â€” keeping that invariant here.
